print('joao')
notas = [5.2, 4.6, 8.1]
provas = 3
total = 0
for nota in notas:
  total = total + nota
media = float(total) / float(provas)
print(media)
if (media <= 6):
  print('reprovado')
elif (media >= 6.1 and media <= 7.9):
  print("reforço")
else:
  print('aprovado')

